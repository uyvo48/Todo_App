package model;

public class CalendarTaskModel {
    private String id;
    private String title;
    private long date;
    private String time;

    public CalendarTaskModel() {
    }

    public CalendarTaskModel(String title, long date, String time) {
        this.title = title;
        this.date = date;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}